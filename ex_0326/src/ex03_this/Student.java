package ex03_this;

public class Student {
	String name;
	int age;
	int studentID;
	
	//생성자
	//객체를 메모리에 올리는거
	//특수한 기능을 가지고 있는 메서드
	public Student(String name, int age, int studentID) {
		this.name = name;
		this.age = age;
		this.studentID = studentID;
	}
	//메서드
	//기능
	public void Student(String name, int age, int studentID) {
		this.name = name;
		this.age = age;
		this.studentID = studentID;
	}
	
	
	
	
}
